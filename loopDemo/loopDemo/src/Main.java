public class Main {
    public static void main(String[] args) {
        //For
        System.out.println("Sistem verileri yükleniyor");
        System.out.println("Geriye kalan süre : 10 Saniye");
        for (int i=10;i>-1;i--)
            System.out.println(i);

        System.out.println("Sistem Verileri Başarıyla Yüklendi");
    }
}